# Zastosowanie funkcji wbudowanych. Wykorzystaj funkcje
# takie jak len, sum, max i min do pracy z listami.

liczby = [4, 8, 2, 6, 1]

# Wyświetl długość listy
print(len(liczby))

# Wyświetl sumę elementów listy
print(sum(liczby))

# Wyświetl maksymalną i minimalną wartość w liście
print(max(liczby), min(liczby))