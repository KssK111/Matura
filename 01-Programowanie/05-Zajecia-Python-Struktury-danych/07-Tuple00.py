# Liczenie ocen w krotce ocen ucznia.
# Stwórz tuple zawierającą oceny ucznia,
# a następnie użyj metody count() do zliczenia wystąpień danej oceny, np.: 5.

oceny_ucznia = (2, 3, 2, 5, 4, 3, 6, 4, 2, 1, 6, 5)
liczba_ocen_ucznia = oceny_ucznia.count(5)
print("Liczba ocen 5:", liczba_ocen_ucznia)
