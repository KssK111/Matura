# Przypisanie wartości do zmiennej za pomocą znaku równa się (=)

# Zmienna liczbowa
liczba_int = 10
print(type(liczba_int))

# Zmienna tekstowa
tekst = 'Hello World!'
print(type(tekst))

# Zmienna rzeczywista
liczba_float = 3.14
print(type(liczba_float))

# Zmienna logiczna
boolean = True
print(type(boolean))